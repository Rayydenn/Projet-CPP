#include "Actor.h"


/////////////////////////////////////////////////////////////////////////////////////////////////////
/*									CONSTRUCTEURS									*/
/////////////////////////////////////////////////////////////////////////////////////////////////////

Actor::Actor():Person()
{
	Id = -1;
}

Actor::Actor(int id, string nom, string prenom):Person(nom,prenom)
{
	Id = id;
}

Actor::~Actor()
{

}

/////////////////////////////////////////////////////////////////////////////////////////////////////
/*									SETTERS									*/
/////////////////////////////////////////////////////////////////////////////////////////////////////

void Actor::setId(int id)
{
	Id = id;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////
/*									GETTERS									*/
/////////////////////////////////////////////////////////////////////////////////////////////////////

int Actor::getId() const
{
	return Id;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////
/*									TUPLE/STRING									*/
/////////////////////////////////////////////////////////////////////////////////////////////////////

string Actor::toString() const
{
	return "Id: " + to_string(Id)
		 + ", Nom: " + lastName
		 + ", Prenom: " + firstName;
}

string Actor::tuple() const
{
	return to_string(Id) + ", " + lastName + ", " + firstName;
}

/////////////////////////////////////////////////////////////////////////////////////////////////////
/*									 OPERATEURS				  									*/
/////////////////////////////////////////////////////////////////////////////////////////////////////


ostream& operator<<(ostream& os, const Actor& a)
{
	os << "<Actor>" << endl;
	os << "<Id>" << endl;
	os << a.getId() << endl;
	os << "</Id>" << endl;
	os << "</Actor>" << endl;

	return os;
}

istream& operator>>(istream& is, Actor& a)
{
	string line;
	getline(is, line);
	getline(is, line);
	getline(is, line);
	a.setId(stoi(line));
	getline(is, line);
	getline(is, line);

	return is;
}
